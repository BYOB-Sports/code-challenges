function generateRandomCourts(): Court[] {
  return Array.from({ length: 60 }, (_, index) => {
    const surface = surfaces[Math.floor(Math.random() * surfaces.length)];
    const indoor = Math.random() > 0.6;
    const lights = Math.random() > 0.3;
    const rating = 3.5 + Math.random() * 1.5; // 3.5 to 5.0
    const reviewCount = Math.floor(Math.random() * 150) + 10;

    return {
      id: `court-${index + 1}`,
      name: courtNames[index % courtNames.length],
      location: locations[Math.floor(Math.random() * locations.length)],
      city: cities[Math.floor(Math.random() * cities.length)],
      state: states[Math.floor(Math.random() * states.length)],
      surface,
      indoor,
      lights,
      price: Math.floor(Math.random() * 80) + 20, // $20-$100
      rating: Math.round(rating * 10) / 10,
      reviewCount,
      image: `https://picsum.photos/400/300?random=${index + 1}`,
      description: `A ${
        indoor ? 'indoor' : 'outdoor'
      } ${surface.toLowerCase()} court facility featuring ${
        lights ? 'excellent lighting' : 'natural lighting'
      } and professional-grade equipment. Perfect for players of all skill levels.`,
      amenities: amenities
        .sort(() => 0.5 - Math.random())
        .slice(0, Math.floor(Math.random() * 8) + 3),
      coordinates: {
        latitude: 25 + Math.random() * 25, // Roughly US coordinates
        longitude: -125 + Math.random() * 50,
      },
    };
  });
}

function generateRandomReviews(courts: Court[]): Review[] {
  const reviews: Review[] = [];

  courts.forEach(court => {
    const reviewCount = Math.min(court.reviewCount, 20); // Limit to 20 reviews per court

    for (let i = 0; i < reviewCount; i++) {
      const rating = Math.floor(Math.random() * 5) + 1;
      const title =
        reviewTitles[Math.floor(Math.random() * reviewTitles.length)];
      const comment =
        reviewComments[Math.floor(Math.random() * reviewComments.length)];
      const userName = userNames[Math.floor(Math.random() * userNames.length)];

      reviews.push({
        id: `review-${court.id}-${i + 1}`,
        courtId: court.id,
        userId: `user-${Math.floor(Math.random() * 1000)}`,
        userName,
        userAvatar: `https://i.pravatar.cc/150?img=${
          Math.floor(Math.random() * 70) + 1
        }`,
        rating,
        title,
        comment,
        date: new Date(
          Date.now() - Math.random() * 365 * 24 * 60 * 60 * 1000,
        ).toISOString(),
        helpful: Math.floor(Math.random() * 20),
        verified: Math.random() > 0.3,
      });
    }
  });

  return reviews;
}

